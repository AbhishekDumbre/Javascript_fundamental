// Bitwise AND
console.log(5 & 1); // 1 (0101 & 0001 → 0001)

// Bitwise OR
console.log(5 | 1); // 5 (0101 | 0001 → 0101)

// Bitwise XOR
console.log(5 ^ 1); // 4 (0101 ^ 0001 → 0100)

// Bitwise NOT
console.log(~5); // -6 (~0101 → 1010 in 2's complement)

// Left shift
console.log(5 << 1); // 10 (0101 << 1 → 1010) in left side we can move number

// Right shift
console.log(5 >> 1); // 2 (0101 >> 1 → 0010)   in right side we can decrease the digit

